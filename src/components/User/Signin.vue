  data() {
    return {
      email: '',
      password: '',
    };
  },
  computed: {
    user() {
      return this.$store.getters.user;
    },
  },
  watch: {
    user(value) {
      if (value !== null && value !== undefined) {
        this.$router.push('/contests');
      }
    },
  },
  methods: {
    onSignin() {
      this.$store.dispatch('signinUser', {
